import logging
from pathlib import Path

Path('logs').mkdir(exist_ok=True)

LOG_FILE = Path('logs/app.log')

logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s',
    handlers=[
        logging.FileHandler(LOG_FILE),
        logging.StreamHandler()
    ]
)
logger = logging.getLogger(__name__)